public class User {
    private  String name;
    private  int age;
    private int card;
    private  String passport;

    public User() {
    }

    public User(String name, int age, int card, String passport) {
        this.name = name;
        this.age = age;
        this.card = card;
        this.passport = passport;
    }

}
